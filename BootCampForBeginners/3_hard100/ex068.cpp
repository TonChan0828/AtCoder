bool myCompare(pair<int, int> a, pair<int, int> b) {
  if (a.first != b.first) {
    return a.first > b.first;
  }
  if (a.second != b.second) {
    return a.second < b.second;
  } else {
    return true;
  }
}

int n;
void dfs(string s, char mx) {
  if (s.length() == n) {
    cout << s << endl;
  } else {
    for (char c = 'a'; c <= mx; ++c) {
      dfs(s + c, ((c == mx) ? (char)(mx + 1) : mx));
    }
  }
}
