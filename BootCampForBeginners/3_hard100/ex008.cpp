bool myCompare(pair<int, int> a, pair<int, int> b)
{
  if (a.first != b.first)
  {
    return a.first > b.first;
  }
  if (a.second != b.second)
  {
    return a.second < b.second;
  }
  else
  {
    return true;
  }
}

int main()
{
  ll n;
  cin >> n;
  if (n % 2 == 1)
  {
    cout << 0 << endl;
  }
  else
  {
    ll ans = 0;
    for (ll i = 5; i * 2 <= n; i = i * 5)
    {
      ans += (n / (i * 2));
    }
    cout << ans << endl;
  }
  return 0;
}
